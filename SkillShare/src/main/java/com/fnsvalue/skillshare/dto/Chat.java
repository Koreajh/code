package com.fnsvalue.skillshare.dto;

public class Chat {
	private int chat_sq_pk;
	private String chat_from;
	private String chat_to;
	private String chat_msg;
	private String chat_dt;
	private String chat_st;
	
	public int getChat_sq_pk() {
		return chat_sq_pk;
	}
	public void setChat_sq_pk(int chat_sq_pk) {
		this.chat_sq_pk = chat_sq_pk;
	}
	public String getChat_from() {
		return chat_from;
	}
	public void setChat_from(String chat_from) {
		this.chat_from = chat_from;
	}
	public String getChat_to() {
		return chat_to;
	}
	public void setChat_to(String chat_to) {
		this.chat_to = chat_to;
	}
	public String getChat_msg() {
		return chat_msg;
	}
	public void setChat_msg(String chat_msg) {
		this.chat_msg = chat_msg;
	}
	public String getChat_dt() {
		return chat_dt;
	}
	public void setChat_dt(String chat_dt) {
		this.chat_dt = chat_dt;
	}
	public String getChat_st() {
		return chat_st;
	}
	public void setChat_st(String chat_st) {
		this.chat_st = chat_st;
	}
	
	

}
